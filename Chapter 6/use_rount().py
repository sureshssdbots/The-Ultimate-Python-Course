# Example 1: Round to nearest integer
number = 7.65432
rounded_number = round(number)
print(rounded_number)  # Output: 8 (Nearest integer)

# Example 2: Round to 2 decimal places
number = 7.65432
rounded_number = round(number, 2)
print(rounded_number)  # Output: 7.65 (Rounded to 2 decimal places)

# Example 3: Round to 1 decimal place
number = 7.65432
rounded_number = round(number, 1)
print(rounded_number)  # Output: 7.7 (Rounded to 1 decimal place)

# Example 4: Round a negative number
number = -7.65432
rounded_number = round(number, 2)
print(rounded_number)  # Output: -7.65 (Rounded to 2 decimal places)
